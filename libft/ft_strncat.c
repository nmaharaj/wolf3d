#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t len)
{
	char	*s1cpy;

	s1cpy = s1;
	while (*s1cpy != '\0')
		s1cpy++;
	while (len-- > 0 && *s2 != '\0')
		*s1cpy++ = *s2++;
	*s1cpy = '\0';
	return (s1);
}
